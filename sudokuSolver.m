% --- Executes on button press in hint.
function hint_Callback(hObject, eventdata, handles)
% hObject    handle to hint (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
state = handles.state;
h = findobj('Tag', 'hinttext');
result = find_Singles(state);
if (~isempty(result))
    r=result(1); c=result(2); value=result(3);
    handles.r=r; handles.c=c; handles.v=value; handles.hint = true;
    guidata(hObject, handles);
    l = ['A','B','C','D','E','F','G','H','I'];
    set(h, 'string', join([l(r),num2str(c),' should be ',num2str(value)],''));
end
if (~handles.hint)
    set(h, 'string', 'No Hints.');
end

